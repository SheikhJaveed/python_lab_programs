def tuple_operations():
    my_tuple = (1, 2, 3)
    print("Original Tuple:", my_tuple)
    
    my_tuple += (4,)  # Adding item
    print("After Adding:", my_tuple)
    
    print("Length of Tuple:", len(my_tuple))
    print("Check for 2 in Tuple:", 2 in my_tuple)
    print("Item at index 1:", my_tuple[1])

tuple_operations()
